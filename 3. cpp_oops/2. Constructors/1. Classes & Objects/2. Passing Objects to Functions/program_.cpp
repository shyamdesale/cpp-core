/*  Passing Objects to Functions -
        1). An object can be given as an argument to a function
        2). Used to process, display, or modify the object's data.
        3). There are three ways to pass an object:
            Pass by Value → object copy; original not changed.
            Pass by Reference → no copy; function works on original object.
            Pass by Pointer → function receives object’s address; useful for dynamic objects.
        4). Pass by reference and pointer are faster (no copying).
        5). Pass by value gives protection (original object safe).
        6). Syntax example: func(obj);, func(&obj);, func(refObj);
*/
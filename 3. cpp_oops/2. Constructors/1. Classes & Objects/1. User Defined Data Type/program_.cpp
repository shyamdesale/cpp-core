/*  User Defined Data Type -
        A data type created by the programmer, not built into C++.
        Used when built-in types (int, float, char) are not enough.
        Allows grouping multiple data items into one custom type.
        Improves code structure, readability, and reusability.
        Examples: class, struct, union, enum, typedef.
        Lets you define your own format for storing and processing data.
*/
/*  Constructors -
        Special function of a class that initializes object data.
        Name of constructor = same as class name.
        No return type, not even void.
        Automatically called when object is created.
        Can be overloaded (multiple constructors).

        Types:
            Default Constructor
            Parameterized Constructor
            Copy Constructor
        Used to set initial values and allocate resources.
        Runs only once per object.
        If no constructor is written, C++ provides a default one.
*/
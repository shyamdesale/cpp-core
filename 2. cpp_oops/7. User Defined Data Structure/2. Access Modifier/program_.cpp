/*  Access Modifiers -
        1. Public :
            Members marked public can be accessed from anywhere in the program.
            Used for functions or variables that must be openly accessible.

            Syntax :
                class ClassName {
                public:
                    // public members
                };

        2. Private :

            Members marked private are accessible only inside the class.
            Not accessible from objects or derived classes.
            Used to hide data and protect internal details (data hiding).

            Syntax :
                class ClassName {
                private:
                    // private members
                };

        3. Protected

            Members marked protected are accessible inside the class and in derived (child) classes.
            Not accessible from outside the class.
            Used mainly in inheritance.

            Syntax :

                class ClassName {
                protected:
                    // protected members
                };
*/
/*  STRING :
        Definition
            A string is a sequence of characters.
            C-style string ends with '\0'.
            C++ uses std::string (dynamic and easier).

    Main Concepts
        Stored in continuous memory.
        Index starts at 0.
        C-style → char str[] (fixed size).
        C++ string → string s (dynamic).
        cin stops at space, getline() takes full line.
        Common operations: length, concat, compare, substring.
*/
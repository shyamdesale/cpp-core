    // Hierarchy of Operators :
    //     The hierarchy of operators (also called operator precedence) in C++ defines the order in which operators are evaluated in an expression.
    //     When multiple operators appear together, the operator with higher precedence is executed first.

    //     Example : int result = 5 + 3 * 2;  - result = 11, not 16

    //     - Basic Order (from highest to lowest):
    //     Parentheses → ()
    //     Unary operators → ++ -- ! ~
    //     Multiplicative → * / %
    //     Additive → + -
    //     Relational → < <= > >=
    //     Equality → == !=
    //     Logical AND / OR → && ||
    //     Assignment → = += -= *= /=
    //     Comma → ,
#include<iostream>
using namespace std;
int main()
{
    int x;
    x = 5;
    cout<<x<<endl;
    x = 8; //Value Update
    cout<<x<<endl;
    x = x + 3; //Update Value + 3
    cout<<x<<endl;
    x = x - 4; //Update Value - 4
    cout<<x<<endl;
    x = x * 6; //Update Value * 6
    cout<<x<<endl;
    x = x / 3; //Update Value / 3
    cout<<x<<endl;
}
#include<iostream>
using namespace std;
int main()
{
    int x;
    x = 5;
    cout<<x<<endl;
    cout<<++x<<endl;
    cout<<++x;
    // This program demonstrates the pre-increment operator (++x) in C++.
    //It increases the value of the variable before it is used, showing how x changes step-by-step from 5 → 6 → 7.
    return 0;
}
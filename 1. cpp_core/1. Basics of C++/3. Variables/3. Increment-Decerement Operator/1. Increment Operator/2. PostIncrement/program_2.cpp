#include<iostream>
using namespace std;
int main()
{
    int x;
    x = 5;
    cout<<x<<endl;
    cout<<x++;
    // This program demonstrates the post-increment operator (x++) in C++.
    // It prints the value before incrementing the variable.
    // After execution, x changes from 5 to 6, but only 5 is shown in the output.
}
#include<iostream>
using namespace std;
int main()
{
    int x;
    x = 5;
    cout<<x<<endl;
    cout<<--x<<endl;
    cout<<x;
    // This program demonstrates the pre-decrement operator (--x) in C++. It prints the initial value of x (5),
    // then decrements x before printing (4), and finally prints the updated value (4).
    return 0;
}
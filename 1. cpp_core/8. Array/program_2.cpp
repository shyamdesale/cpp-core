#include<iostream>
using namespace std;

int main()
{
    // Step 1: Declare an array 'arr' of size 5 to store integers
    int arr[5];

    // Step 2: Assign values to each element of the array
    arr[0] = 6;   // First element
    arr[1] = 3;   // Second element
    arr[2] = 7;   // Third element
    arr[3] = 2;   // Fourth element
    arr[4] = 8;   // Fifth element

    // Step 3: Print all array elements
    cout << arr[0] << "  ";  // Print first element
    cout << arr[1] << "  ";  // Print second element
    cout << arr[2] << "  ";  // Print third element
    cout << arr[3] << "  ";  // Print fourth element
    cout << arr[4] << "  ";  // Print fifth element

    return 0;
}

/*  Algorithm
        Declare an array of 5 integers.
        Initialize each element of the array with a specific value: 6, 3, 7, 2, 8.
        Print each element of the array in order, separated by spaces.
        End the program.
*/
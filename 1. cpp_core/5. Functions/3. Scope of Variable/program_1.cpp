/*  Scope of Variable in C++

        1.  Local Scope - Variable works only inside the function/block where it is declared.
        2.  Global Scope - Variable declared outside functions is accessible everywhere in the file.
        3.  Block Scope - Anything inside { } exists only till that block ends.
        4.  Class Scope - Class members are accessed using objects and access specifiers.
        5.  Static Scope - static keeps the variable local but makes its lifetime the entire program.
*/